# Some of the commonly used libraries used by Data Scientists include:
# 1. Pandas: A foundational library for data manipulation and analysis in Python. Pandas provide easy-to-use data structures and data analysis tools, making it ideal for tasks like data cleaning, transformation, and aggregation.
# 2. NumPy: Essential for scientific computing in Python, NumPy offers support for large, multi-dimensional arrays and matrices, along with a collection of mathematical functions to operate on these arrays. It's widely used for numerical data processing.
# 3. Scikit-learn: A key library for machine learning in Python. Scikit-learn provides a range of supervised and unsupervised learning algorithms via a consistent interface. It's known for its simplicity in implementing complex machine learning models.

